package main

import "fmt"

func main() {
	slicel := make([]int, 5, 10)
	slicel = append(slicel, 5, 5, 5, 5, 5)
	fmt.Println(slicel)
	fmt.Printf("len: %d, cap: %d\n", len(slicel), cap(slicel))
	slicel = append(slicel, 1)
	fmt.Println(slicel)
	fmt.Printf("len: %d, cap: %d\n", len(slicel), cap(slicel))
}
